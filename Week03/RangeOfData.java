package Week03;
public class RangeOfData{
    public static void main(String[] args) {
        System.out.println("Data type of byte \t=> \tMax = "+Byte.MAX_VALUE+ "\t\t\tMin = "+Byte.MIN_VALUE);
        System.out.println("Data type of short \t=> \tMax = "+Short.MAX_VALUE+ "\t\t\tMin = "+Short.MIN_VALUE);
        System.out.println("Data type of int \t=> \tMax = "+Integer.MAX_VALUE+ "\t\tMin = "+Integer.MIN_VALUE);
        System.out.println("Data type of long \t=> \tMax = "+Long.MAX_VALUE+ "\tMin = "+Long.MIN_VALUE);
        System.out.println("Data type of float \t=> \tMax = "+Float.MAX_VALUE+ "\t\tMin = "+Float.MIN_VALUE);
        System.out.println("Data type of double \t=> \tMax = "+Double.MAX_VALUE+ "\tMin = "+Double.MIN_VALUE);
    }
}